# 學習所有程式語言的第一個練習題
# 請寫一個程式，可以讀入指定的字串，並且輸出指定的字串。
# 比如：輸入字串 "world", 則請輸出 "hello, world"

# Input
# 輸入總共一行，內含一組文字

# Output
# 輸出題目指定的文字。

# Sample Input #1
# world

# Sample Output #1
# hello, world

# Sample Input #2
# C++

# Sample Output #2
# hello, C++

# Sample Input #3
# Taiwan

# Sample Output #3
# hello, Taiwan

def main():
    name = input()
    print(f"hello, {name}")

main()
